package example.composite.pattern;

public class DependentObject1 {

	private String data;

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public DependentObject1(String data) {
		super();
		this.data = data;
	}

	public DependentObject1() {
		super();
	}
	
	

}
