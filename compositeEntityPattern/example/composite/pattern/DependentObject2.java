package example.composite.pattern;

public class DependentObject2 {
	
	private String data;

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public DependentObject2() {
		super();
	}

	public DependentObject2(String data) {
		super();
		this.data = data;
	}
	
	
	

}
